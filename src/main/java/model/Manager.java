package model;

/**
 * @author vagrant
 *
 */
public class Manager implements Domain {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String name;
	private String empId;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the empId
	 */
	public String getEmpId() {
		return empId;
	}

	/**
	 * @param empId
	 *            the empId to set
	 */
	public void setEmpId(String empId) {
		this.empId = empId;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Manager [name=" + name + ", empId=" + empId + "]";
	}

}
